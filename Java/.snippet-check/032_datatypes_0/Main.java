public class Main {
    public static void main(String[] args) {
        // Integer types
        byte age = 25;
        short year = 2024;
        int salary = 100_000;        // Underscores for readability!
        long worldPop = 8_000_000_000L; // Must end with L for long

        // Floating-point types
        float pi = 3.14159f;        // Must end with f for float
        double precise = 3.141592653589793; // Default decimal type

        // Boolean & Char
        boolean isJavaFun = true;
        char grade = 'A';
        char unicode = '\u0041';    // Also 'A' (Unicode)

        // Number systems
        int hex = 0xFF;              // Hexadecimal = 255
        int binary = 0b1010;         // Binary = 10
        int octal = 077;             // Octal = 63
    }
}